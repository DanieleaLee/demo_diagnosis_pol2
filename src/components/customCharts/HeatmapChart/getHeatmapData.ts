const cnt = 10;
const getRandomNum = (min, max) => {
  min = Math.ceil(min);
  max = Math.floor(max);
  return Math.random() * (max - min) + min; //최댓값도 포함, 최솟값도 포함
};

const fakeRawData = Array(cnt)
  .fill(0)
  .map((v, i) => {
    const yPosition = i;
    const data = Array(cnt)
      .fill(yPosition)
      .map((j, k) => {
        // yPosition, xPosition, correlation
        return [j, k, getRandomNum(-1, 1)];
      });
    return data;
  })
  .flat();

